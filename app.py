# are too large to commit to the GitHub repo (109MB and 128MB respectively,
# over GitHub's comfortable limits). On Streamlit Community Cloud, these are
# downloaded once on app startup and cached. For local development, if the
# files already exist in the working directory (e.g. freshly generated by
# running pipeline.py), those are used directly instead of re-downloading.
#
# IMPORTANT: replace YOUR_USERNAME below with your actual Hugging Face
# username once the dataset is uploaded.
HF_BASE_URL = "https://huggingface.co/datasets/baibhavbaidya/parkwatch-data/resolve/main"
GEO_SCORES_URL = f"{HF_BASE_URL}/geo_cell_scores.csv" 
VIOLATIONS_URL = f"{HF_BASE_URL}/cleaned_violations.csv" 

LOCAL_GEO_SCORES = "geo_cell_scores.csv"
LOCAL_VIOLATIONS = "cleaned_violations.csv"


def download_if_needed(url, local_path):
    """Download a file from a URL to a local cache path if not already present."""
    if os.path.exists(local_path):
        return local_path
    cache_path = f"/tmp/{os.path.basename(local_path)}" if not os.access(".", os.W_OK) else local_path
    if os.path.exists(cache_path):
        return cache_path
    response = requests.get(url, stream=True, timeout=120)
    response.raise_for_status()
    with open(cache_path, "wb") as f:
        for chunk in response.iter_content(chunk_size=8192):
            f.write(chunk)
    return cache_path


@st.cache_data(show_spinner="Loading violation data (first load may take a moment)...")
def load_data():
    # Prefer local files (fast path for local dev after running pipeline.py)
    if os.path.exists(LOCAL_GEO_SCORES) and os.path.exists(LOCAL_VIOLATIONS):
        geo_path = LOCAL_GEO_SCORES
        viol_path = LOCAL_VIOLATIONS
    else:
        geo_path = download_if_needed(GEO_SCORES_URL, LOCAL_GEO_SCORES)
        viol_path = download_if_needed(VIOLATIONS_URL, LOCAL_VIOLATIONS)

    agg = pd.read_csv(geo_path)
    violations = pd.read_csv(viol_path)
    violations['violations_list'] = violations['violations_list'].apply(
        lambda x: ast.literal_eval(x) if isinstance(x, str) else []
    )
    return agg, violations

agg, violations = load_data()

# Hotspots = cells with enough volume to be statistically meaningful
hotspots = agg[agg['violation_count'] >= 20].copy()
hotspots = hotspots.sort_values('congestion_impact_score', ascending=False).reset_index(drop=True)

